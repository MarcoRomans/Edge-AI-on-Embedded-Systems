#!/usr/bin/env python3
"""
Baseline benchmark launcher for Qwen2.5 GGUF models on BeagleBone AI-64.

This file documents the interface used in the experiments. In the experimental
setup, the script invoked llama.cpp (`llama-completion`) on a TSV prompt set and
saved raw CSV results plus summary CSV files.

Expected arguments:
  --model-label
  --model-path
  --temperature
  --repeats
  --max-prompts

The full raw result files generated by the experiments are stored in `raw_data/`.
"""

import argparse
from pathlib import Path

def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--model-label", required=True)
    parser.add_argument("--model-path", required=True)
    parser.add_argument("--temperature", type=float, required=True)
    parser.add_argument("--repeats", type=int, default=3)
    parser.add_argument("--max-prompts", type=int, default=None)
    args = parser.parse_args()

    print("Baseline benchmark configuration")
    print("model_label:", args.model_label)
    print("model_path:", args.model_path)
    print("temperature:", args.temperature)
    print("repeats:", args.repeats)
    print("max_prompts:", args.max_prompts)
    print()
    print("This repository stores the generated CSV results in raw_data/.")
    print("Run the original board-side benchmark script in the project directory to reproduce the measurements.")

if __name__ == "__main__":
    main()
